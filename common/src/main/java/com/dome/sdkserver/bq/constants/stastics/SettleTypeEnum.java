package com.dome.sdkserver.bq.constants.stastics;

/**
 * 结算类型枚举
 * @author xuefeihu
 *
 */
public enum SettleTypeEnum {

	/**
	 * 未结算
	 */
	NOT_SETTLE(10,"未结算"),
	
	/**
	 * 已结算
	 */
	SETTLEED(20,"已结算");
	
	private SettleTypeEnum(int type,String desc){
		this.type = type;
		this.desc = desc;
	}
	
	private int type;
	
	private String desc;

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
	
	
}
