package com.dome.sdkserver.bq.constants;
/**
 * 钱宝账户根据扣款类型查询可用金额常量类
 * @author liuxingyue
 *
 */
public class QbAccountDeductType {

    /**
     * qb扣款 ，不能使用冻结金额和信用卡额度，不需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_1 = 1;   
    
    /**
     * qb 扣款，不能使用冻结金额，不需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_2 = 2;      
    
    /**
     * qb 扣款，不能使用信用卡额度，不需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_3 = 3;       
    
    /**
     * qb 扣款，能使用信用卡额度和冻结金额，不需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_4 = 4;     
  
    /**
     *  可用冻结金额，不能用信用卡额度，需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_5 = 5;         
    
    
    /**
     *  不可用冻结金额，不能用信用卡额度，需要考虑不可用金额
     */
    public static final int DEDUCT_TYPE_QB_6 = 6;   

}
