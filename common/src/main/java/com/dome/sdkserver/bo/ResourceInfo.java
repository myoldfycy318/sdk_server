package com.dome.sdkserver.bo;

import java.util.Date;

/**
 * 资源信息表
 * @author hexiaoyi
 */
public class ResourceInfo {
	/** 资源ID */
	private Integer resourceId;
	/** 资源标题 */
	private String resourceTitle;
	/** 资源类型：10 文档 20 sdk */
	private String resourceType;
	/** 栏目：10 联合登录 20 统一支付 */
	private String resourceColumn;
	/** 图片服务器url */
	private String resourceUrl;
	/** 文件大小 */
	private String fileSize;
	/** 状态：1 待发布 2 已发布 3 已下线 */
	private String status;
	/** 删除标识：0 未删除 1 已删除 */
	private int delFlag;
	/** 显示更新时间 */
	private String showUpdateTime;
	/** 创建时间 */
	private Date createTime;
	/** 更新时间 */
	private Date updateTime;

	public Integer getResourceId() {
		return resourceId;
	}

	public void setResourceId(Integer resourceId) {
		this.resourceId = resourceId;
	}

	public String getResourceTitle() {
		return resourceTitle;
	}

	public void setResourceTitle(String resourceTitle) {
		this.resourceTitle = resourceTitle;
	}

	public String getResourceType() {
		return resourceType;
	}

	public void setResourceType(String resourceType) {
		this.resourceType = resourceType;
	}

	public String getResourceColumn() {
		return resourceColumn;
	}

	public void setResourceColumn(String resourceColumn) {
		this.resourceColumn = resourceColumn;
	}

	public String getResourceUrl() {
		return resourceUrl;
	}

	public void setResourceUrl(String resourceUrl) {
		this.resourceUrl = resourceUrl;
	}

	public String getFileSize() {
		return fileSize;
	}

	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getDelFlag() {
		return delFlag;
	}

	public void setDelFlag(int delFlag) {
		this.delFlag = delFlag;
	}

	public String getShowUpdateTime() {
		return showUpdateTime;
	}

	public void setShowUpdateTime(String showUpdateTime) {
		this.showUpdateTime = showUpdateTime;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
}
