package com.dome.sdkserver.bo.statistic;

/**
 * 应用用户每天的统计信息
 * 新增注册用户数、登陆用户数、总用户数
 * @author lilongwei
 *
 */
public class AppUserDayStatisticInfo {

	/**
	 * 商户编码
	 */
	private String merchantCode;
	
	/**
	 * 应用编码
	 */
	private String appCode;
	
	/**
	 * 日期格式yyyy-MM-dd
	 */
	private String date;
	
	/**
	 * 新增注册用户数
	 */
	private int registerUsersSum;
	
	/**
	 * 登陆用户数
	 */
	private int loginUsersSum;
	
	/**
	 * 总用户数
	 */
	private int usersSum;

	public String getMerchantCode() {
		return merchantCode;
	}

	public void setMerchantCode(String merchantCode) {
		this.merchantCode = merchantCode;
	}

	public String getAppCode() {
		return appCode;
	}

	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getRegisterUsersSum() {
		return registerUsersSum;
	}

	public void setRegisterUsersSum(int registerUsersSum) {
		this.registerUsersSum = registerUsersSum;
	}

	public int getLoginUsersSum() {
		return loginUsersSum;
	}

	public void setLoginUsersSum(int loginUsersSum) {
		this.loginUsersSum = loginUsersSum;
	}

	public int getUsersSum() {
		return usersSum;
	}

	public void setUsersSum(int usersSum) {
		this.usersSum = usersSum;
	}

	@Override
	public String toString() {
		return "AppUserDayStatisticInfo [merchantCode=" + merchantCode
				+ ", appCode=" + appCode + ", date=" + date
				+ ", registerUsersSum=" + registerUsersSum + ", loginUsersSum="
				+ loginUsersSum + ", usersSum=" + usersSum + "]";
	}
	
	
	
}
