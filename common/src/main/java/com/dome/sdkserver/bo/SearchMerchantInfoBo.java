package com.dome.sdkserver.bo;

/**
 * 商户查询业务对象
 * @author hexiaoyi
 */
public class SearchMerchantInfoBo {
	/** 公司全称 */
	private String merchantFullName;
	/** 申请时间（开始） */
	private String applyStartDate;
	/** 申请时间（结束） */
	private String applyEndDate;
	/** 状态 */
	private String status;
	/** 联系人 */
	private String contacts;
	/** 手机号码 */
	private String mobilePhoneNum;
	/** 每页数据量 */
	private Integer size;
	/** 开始查询点 */
	private Integer start;
	
	public SearchMerchantInfoBo(){}
	
	public SearchMerchantInfoBo(String merchantFullName,String applyStartDate
			,String applyEndDate,String status,String contacts,String mobilePhoneNum){
		this.merchantFullName = merchantFullName;
		this.applyStartDate = applyStartDate;
		this.applyEndDate = applyEndDate;
		this.status = status;
		this.contacts = contacts;
		this.mobilePhoneNum = mobilePhoneNum;
	}

	public String getMerchantFullName() {
		return merchantFullName;
	}

	public void setMerchantFullName(String merchantFullName) {
		this.merchantFullName = merchantFullName;
	}

	public String getApplyStartDate() {
		return applyStartDate;
	}

	public void setApplyStartDate(String applyStartDate) {
		this.applyStartDate = applyStartDate;
	}

	public String getApplyEndDate() {
		return applyEndDate;
	}

	public void setApplyEndDate(String applyEndDate) {
		this.applyEndDate = applyEndDate;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getContacts() {
		return contacts;
	}

	public void setContacts(String contacts) {
		this.contacts = contacts;
	}

	public String getMobilePhoneNum() {
		return mobilePhoneNum;
	}

	public void setMobilePhoneNum(String mobilePhoneNum) {
		this.mobilePhoneNum = mobilePhoneNum;
	}

	public Integer getSize() {
		return size;
	}

	public void setSize(Integer size) {
		this.size = size;
	}

	public Integer getStart() {
		return start;
	}

	public void setStart(Integer start) {
		this.start = start;
	}
	
}
