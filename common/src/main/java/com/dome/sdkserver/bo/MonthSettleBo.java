package com.dome.sdkserver.bo;

import java.math.BigDecimal;
import java.util.List;

/**
 * 
 * @author Frank.Zhou
 *
 */
public class MonthSettleBo {

	private BigDecimal totalSumAmount;

	private BigDecimal totalSettleAmount;

	private BigDecimal totlePayAmount;

	private List<SettleInfo> list;

	public BigDecimal getTotalSumAmount() {
		return totalSumAmount;
	}

	public void setTotalSumAmount(BigDecimal totalSumAmount) {
		this.totalSumAmount = totalSumAmount;
	}

	public BigDecimal getTotalSettleAmount() {
		return totalSettleAmount;
	}

	public void setTotalSettleAmount(BigDecimal totalSettleAmount) {
		this.totalSettleAmount = totalSettleAmount;
	}

	public BigDecimal getTotlePayAmount() {
		return totlePayAmount;
	}

	public void setTotlePayAmount(BigDecimal totlePayAmount) {
		this.totlePayAmount = totlePayAmount;
	}

	public List<SettleInfo> getList() {
		return list;
	}

	public void setList(List<SettleInfo> list) {
		this.list = list;
	}

}
