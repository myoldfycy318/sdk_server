package com.dome.sdkserver.service.pay.qbao.bo;

/**
 * @author mazhongmin
 * 
 *
 */
public class AcountDetail {

	/**账户变动类型*/
	private int accountChangeType;
	
	/**操作账户类型*/
	private int accountType;
	
	/**账户变动金额**/
	private double amount;
	
	/**关联id，预冻结确认支付，退款时有意义.,正常交易可不传**/
	private String relatedId;
	
	/**子流水操作对应的用户id**/
	private long userId;

	public int getAccountChangeType() {
		return accountChangeType;
	}

	public void setAccountChangeType(int accountChangeType) {
		this.accountChangeType = accountChangeType;
	}

	public int getAccountType() {
		return accountType;
	}

	public void setAccountType(int accountType) {
		this.accountType = accountType;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

	public String getRelatedId() {
		return relatedId;
	}

	public void setRelatedId(String relatedId) {
		this.relatedId = relatedId;
	}

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}
	
}
