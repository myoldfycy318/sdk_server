package com.dome.sdkserver.service.pay.qbao.bo;

import java.math.BigDecimal;

/**
 * 应用市场大转盘添加或更新规则信息请求参数
 * @author liuxingyue
 *
 */
public class TurnableRuleRequest{
	/**
     * 规则id
     */
    private Integer id;

    /**
     * 规则名称.
     */
    private String name;

    /**
     * 开始时间.
     */
    private String startTime;

    /**
     * 结束时间.
     */
    private String endTime;


    /**
     * 抽奖次数.
     */
    private Integer number;

        /**
     * 充值规则类型：0 单笔、1 每日、2 每周、3 每月、4 时间范围.
     */
    private Byte rechargeType;

    /**
     * 开始时间.
     */
    private String rechargeStartTime;

    /**
     * 结束时间.
     */
    private String rechargeEndTime;

    /**
     * 游戏ID
     */
    private String rechargeGameIds;

    /**
     * 条件类型：0 >=、1 =、2 >.
     */
    private Byte rechargeCondition;

    /**
     * 宝币数量.
     */
    private BigDecimal rechargeCoinAmount;

    /**
     * 宝券数量.
     */
    private BigDecimal rechargeBqAmount;

    /**
     * 请求ip
     */
    private String requestIp;

	public String getRequestIp() {
		return requestIp;
	}

	public void setRequestIp(String requestIp) {
		this.requestIp = requestIp;
	}
	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


	public Integer getNumber() {
		return number;
	}

	public void setNumber(Integer number) {
		this.number = number;
	}

	public Byte getRechargeType() {
		return rechargeType;
	}

	public void setRechargeType(Byte rechargeType) {
		this.rechargeType = rechargeType;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public String getRechargeStartTime() {
		return rechargeStartTime;
	}

	public void setRechargeStartTime(String rechargeStartTime) {
		this.rechargeStartTime = rechargeStartTime;
	}

	public String getRechargeEndTime() {
		return rechargeEndTime;
	}

	public void setRechargeEndTime(String rechargeEndTime) {
		this.rechargeEndTime = rechargeEndTime;
	}

	public String getRechargeGameIds() {
		return rechargeGameIds;
	}

	public void setRechargeGameIds(String rechargeGameIds) {
		this.rechargeGameIds = rechargeGameIds;
	}

	public Byte getRechargeCondition() {
		return rechargeCondition;
	}

	public void setRechargeCondition(Byte rechargeCondition) {
		this.rechargeCondition = rechargeCondition;
	}

	public BigDecimal getRechargeCoinAmount() {
		return rechargeCoinAmount;
	}

	public void setRechargeCoinAmount(BigDecimal rechargeCoinAmount) {
		this.rechargeCoinAmount = rechargeCoinAmount;
	}

	public BigDecimal getRechargeBqAmount() {
		return rechargeBqAmount;
	}

	public void setRechargeBqAmount(BigDecimal rechargeBqAmount) {
		this.rechargeBqAmount = rechargeBqAmount;
	}
}
