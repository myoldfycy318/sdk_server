package com.dome.sdkserver.service.pay.qbao.bo;

import java.math.BigDecimal;

public class AccountResultData {
	private String flowId;
	private String requestIds;
	private BigDecimal qbAmount;
	private BigDecimal bqAmount;
	public String getFlowId() {
		return flowId;
	}
	public void setFlowId(String flowId) {
		this.flowId = flowId;
	}
	public String getRequestIds() {
		return requestIds;
	}
	public void setRequestIds(String requestIds) {
		this.requestIds = requestIds;
	}
	public BigDecimal getQbAmount() {
		return qbAmount;
	}
	public void setQbAmount(BigDecimal qbAmount) {
		this.qbAmount = qbAmount;
	}
	public BigDecimal getBqAmount() {
		return bqAmount;
	}
	public void setBqAmount(BigDecimal bqAmount) {
		this.bqAmount = bqAmount;
	}
}
