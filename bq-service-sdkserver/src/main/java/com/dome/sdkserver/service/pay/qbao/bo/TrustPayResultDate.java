package com.dome.sdkserver.service.pay.qbao.bo;

import java.math.BigDecimal;

/**
 * 信任支付返回参数
 * @author liuxingyue
 *
 */
public class TrustPayResultDate {

	private long sdkFlowId;  
	
	private String orderNo;
	
	private String transDate;
	
	private BigDecimal qbAmount;
	
	private BigDecimal bqAmount;

	public TrustPayResultDate() {
		
	}

	public TrustPayResultDate(long sdkFlowId, String orderNo, String transDate, BigDecimal qbAmount,
			BigDecimal bqAmount) {
		this.sdkFlowId = sdkFlowId;
		this.orderNo = orderNo;
		this.transDate = transDate;
		this.qbAmount = qbAmount;
		this.bqAmount = bqAmount;
	}

	public long getSdkFlowId() {
		return sdkFlowId;
	}

	public void setSdkFlowId(long sdkFlowId) {
		this.sdkFlowId = sdkFlowId;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public String getTransDate() {
		return transDate;
	}

	public void setTransDate(String transDate) {
		this.transDate = transDate;
	}

	public BigDecimal getQbAmount() {
		return qbAmount;
	}

	public void setQbAmount(BigDecimal qbAmount) {
		this.qbAmount = qbAmount;
	}

	public BigDecimal getBqAmount() {
		return bqAmount;
	}

	public void setBqAmount(BigDecimal bqAmount) {
		this.bqAmount = bqAmount;
	}
	
	
	
}
