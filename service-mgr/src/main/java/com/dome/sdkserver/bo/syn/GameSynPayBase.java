package com.dome.sdkserver.bo.syn;

public class GameSynPayBase {

	private String appCode;
	
	private String appName;
	
	private String channelCode;
	
	private int status;
	
	private int merchantId;
	
	private String payNotifyUrl;
	
	private String testPayNotifyUrl;

	public String getAppCode() {
		return appCode;
	}

	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public String getChannelCode() {
		return channelCode;
	}

	public void setChannelCode(String channelCode) {
		this.channelCode = channelCode;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getMerchantId() {
		return merchantId;
	}

	public void setMerchantId(int merchantId) {
		this.merchantId = merchantId;
	}

	public String getPayNotifyUrl() {
		return payNotifyUrl;
	}

	public void setPayNotifyUrl(String payNotifyUrl) {
		this.payNotifyUrl = payNotifyUrl;
	}

	public String getTestPayNotifyUrl() {
		return testPayNotifyUrl;
	}

	public void setTestPayNotifyUrl(String testPayNotifyUrl) {
		this.testPayNotifyUrl = testPayNotifyUrl;
	}
	
	
}
