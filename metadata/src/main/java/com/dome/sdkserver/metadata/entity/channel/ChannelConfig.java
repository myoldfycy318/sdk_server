package com.dome.sdkserver.metadata.entity.channel;

import java.math.BigDecimal;

import com.dome.sdkserver.constants.channel.CooperTypeEnum;

public class ChannelConfig {

	private CooperTypeEnum cooperType;
	
	private BigDecimal dividePercent;
	
	private BigDecimal activityUnitPrice;
	
    /*扣量比例*/
    private BigDecimal discount;
    /*激活量阀值*/
    private int activeThreshold;
    /*充值金额扣量比例*/
    private BigDecimal rechargeAmountDiscount;
    /*付费用户扣量比例*/
    private BigDecimal payingUserDiscount;
    /*充值量阀值*/
    private BigDecimal payingThreshold;
	public CooperTypeEnum getCooperType() {
		return cooperType;
	}
	public void setCooperType(CooperTypeEnum cooperType) {
		this.cooperType = cooperType;
	}
	public BigDecimal getDividePercent() {
		return dividePercent;
	}
	public void setDividePercent(BigDecimal dividePercent) {
		this.dividePercent = dividePercent;
	}
	public BigDecimal getActivityUnitPrice() {
		return activityUnitPrice;
	}
	public void setActivityUnitPrice(BigDecimal activityUnitPrice) {
		this.activityUnitPrice = activityUnitPrice;
	}
	public BigDecimal getDiscount() {
		return discount;
	}
	public void setDiscount(BigDecimal discount) {
		this.discount = discount;
	}
	public int getActiveThreshold() {
		return activeThreshold;
	}
	public void setActiveThreshold(int activeThreshold) {
		this.activeThreshold = activeThreshold;
	}
	public BigDecimal getRechargeAmountDiscount() {
		return rechargeAmountDiscount;
	}
	public void setRechargeAmountDiscount(BigDecimal rechargeAmountDiscount) {
		this.rechargeAmountDiscount = rechargeAmountDiscount;
	}
	public BigDecimal getPayingUserDiscount() {
		return payingUserDiscount;
	}
	public void setPayingUserDiscount(BigDecimal payingUserDiscount) {
		this.payingUserDiscount = payingUserDiscount;
	}
	public BigDecimal getPayingThreshold() {
		return payingThreshold;
	}
	public void setPayingThreshold(BigDecimal payingThreshold) {
		this.payingThreshold = payingThreshold;
	}
    
    
}
