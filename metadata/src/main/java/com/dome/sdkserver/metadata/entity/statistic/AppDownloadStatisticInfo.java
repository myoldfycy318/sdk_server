package com.dome.sdkserver.metadata.entity.statistic;

/**
 * 应用下载统计数据封装类
 * 日流水，存放了用户产出界面需要的所有数据
 * 月流水，像付费转化率，arpu, arppu等不做统计
 * @author lilongwei
 *
 */
public class AppDownloadStatisticInfo {

	/**
	 * 日统计=downDate，月统计=downMonth
	 */
	private String dateCol;
	
    /**
     * 下载日期
     */
    private String downDate;
    
    /**
     * 下载月份.
     */
    private String downMonth;
    
    /**
     * 应用市场appid.
     */
    private Integer appId;

    /**
     * 下载次数.
     */
    private Integer downTimes = 0;

    /**
     * 下载用户数.
     */
    private Integer userCounts = 0;
    
	/**
	 * 付费用户数
	 */
	private int payUserCount;
	
	/**
	 * 付费次数
	 */
	private long payCount;
	
	private String merchantCode;
	private String merchantName;
	private String appCode;
	private String appName;
	private int appSource;
    
    private double payRate;
    private double arpu;
    private double arppu;
    
    private long qbbFlow;
    
    private long bqFlow;
    private double qbbCash;
    private double bqCash;
    private double cashSum;
	public String getDateCol() {
		return dateCol;
	}

	public void setDateCol(String dateCol) {
		this.dateCol = dateCol;
	}

	public String getDownDate() {
		return downDate;
	}

	public void setDownDate(String downDate) {
		this.downDate = downDate;
	}

	public String getDownMonth() {
		return downMonth;
	}

	public void setDownMonth(String downMonth) {
		this.downMonth = downMonth;
	}

	public Integer getAppId() {
		return appId;
	}

	public void setAppId(Integer appId) {
		this.appId = appId;
	}

	public String getAppCode() {
		return appCode;
	}

	public void setAppCode(String appCode) {
		this.appCode = appCode;
	}

	public Integer getDownTimes() {
		return downTimes;
	}

	public void setDownTimes(Integer downTimes) {
		this.downTimes = downTimes;
	}

	public Integer getUserCounts() {
		return userCounts;
	}

	public void setUserCounts(Integer userCounts) {
		this.userCounts = userCounts;
	}

	public String getMerchantCode() {
		return merchantCode;
	}

	public void setMerchantCode(String merchantCode) {
		this.merchantCode = merchantCode;
	}

	public int getPayUserCount() {
		return payUserCount;
	}

	public void setPayUserCount(int payUserCount) {
		this.payUserCount = payUserCount;
	}

	public long getPayCount() {
		return payCount;
	}

	public void setPayCount(long payCount) {
		this.payCount = payCount;
	}

	public String getMerchantName() {
		return merchantName;
	}

	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}

	public String getAppName() {
		return appName;
	}

	public void setAppName(String appName) {
		this.appName = appName;
	}

	public int getAppSource() {
		return appSource;
	}

	public void setAppSource(int appSource) {
		this.appSource = appSource;
	}

	public double getPayRate() {
		return payRate;
	}

	public void setPayRate(double payRate) {
		this.payRate = payRate;
	}


	public double getArpu() {
		return arpu;
	}

	public void setArpu(double arpu) {
		this.arpu = arpu;
	}

	public double getArppu() {
		return arppu;
	}

	public void setArppu(double arppu) {
		this.arppu = arppu;
	}

	public long getQbbFlow() {
		return qbbFlow;
	}

	public void setQbbFlow(long qbbFlow) {
		this.qbbFlow = qbbFlow;
	}

	public long getBqFlow() {
		return bqFlow;
	}

	public void setBqFlow(long bqFlow) {
		this.bqFlow = bqFlow;
	}

	public double getQbbCash() {
		return qbbCash;
	}

	public void setQbbCash(double qbbCash) {
		this.qbbCash = qbbCash;
	}

	public double getBqCash() {
		return bqCash;
	}

	public void setBqCash(double bqCash) {
		this.bqCash = bqCash;
	}

	public double getCashSum() {
		return cashSum;
	}

	public void setCashSum(double cashSum) {
		this.cashSum = cashSum;
	}
	
}
