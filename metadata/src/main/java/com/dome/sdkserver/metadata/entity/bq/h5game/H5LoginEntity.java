package com.dome.sdkserver.metadata.entity.bq.h5game;

/**
 * H5LoginEntity
 *
 * @author Zhang ShanMin
 * @date 2016/9/25
 */
public class H5LoginEntity {

    private String cpGameUrl;
    private String userId;
    private String zoneId;
    private String appCode;
    private String isAdult;
    /**
     * userId是否加密
     * true:加密| false:未
     */
    private String encryptUi;
    //冰趣H5游戏嵌入平台
    private String platformCode;

    public String getCpGameUrl() {
        return cpGameUrl;
    }

    public void setCpGameUrl(String cpGameUrl) {
        this.cpGameUrl = cpGameUrl;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getZoneId() {
        return zoneId;
    }

    public void setZoneId(String zoneId) {
        this.zoneId = zoneId;
    }

    public String getAppCode() {
        return appCode;
    }

    public void setAppCode(String appCode) {
        this.appCode = appCode;
    }

    public String getIsAdult() {
        return isAdult;
    }

    public void setIsAdult(String isAdult) {
        this.isAdult = isAdult;
    }

    public String getEncryptUi() {
        return encryptUi;
    }

    public void setEncryptUi(String encryptUi) {
        this.encryptUi = encryptUi;
    }

    public String getPlatformCode() {
        return platformCode;
    }

    public void setPlatformCode(String platformCode) {
        this.platformCode = platformCode;
    }
}
